public class intLimitProgram {
    public static void main(String[] args) {
        int minValueOfInt = Integer.MIN_VALUE;
        int maxValueOfInt = Integer.MAX_VALUE;

        System.out.println("Min value:"+minValueOfInt);
        System.out.println("Min value:"+maxValueOfInt);
        // MAX VALUE + 1 = MIN_VALUE
        // MIN VALUE - 1 = MAX_VALUE
    }
}
